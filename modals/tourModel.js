tourSchema.pre(/^find/, function (next) {
  this.find({ secretTour: { $ne: true } });
  next();
});

tourSchema.pre(/^find/, function (next) {
  // this points to the query generated by find methods
  this.populate('guides', '-__v -passwordChangedAt');

  next();
});

